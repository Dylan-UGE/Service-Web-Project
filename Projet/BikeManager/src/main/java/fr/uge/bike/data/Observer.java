package fr.uge.bike.data;

import fr.uge.bike.interf.IObserver;

import java.rmi.RemoteException;
import java.rmi.server.UnicastRemoteObject;

public class Observer extends UnicastRemoteObject implements IObserver {
    protected Observer() throws RemoteException {
    }

    @Override
    public void notifyChange(User user) throws RemoteException {
        System.out.println("L'utilisateur "+user.getUsername()+"a loué un vélo");
    }
}




























































































































































































































































